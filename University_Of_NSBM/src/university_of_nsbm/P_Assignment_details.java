
package university_of_nsbm;


public class P_Assignment_details {

    
    public int getRegID() {
        return regID;
    }

    
    public void setRegID(int regID) {
        this.regID = regID;
    }

    
    public String getFullName() {
        return fullName;
    }

    
    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    
    public String getYear() {
        return year;
    }

   
    public void setYear(String year) {
        this.year = year;
    }

    
    public String getFacalty() {
        return facalty;
    }

    
    public void setFacalty(String facalty) {
        this.facalty = facalty;
    }

   
    public String getSubject() {
        return subject;
    }

    
    public void setSubject(String subject) {
        this.subject = subject;
    }

    
    public String getCode() {
        return code;
    }

    
    public void setCode(String code) {
        this.code = code;
    }

    
    public String getAssignmentNo() {
        return assignmentNo;
    }

    
    public void setAssignmentNo(String assignmentNo) {
        this.assignmentNo = assignmentNo;
    }

    
    public String getGrade() {
        return grade;
    }

    
    public void setGrade(String grade) {
        this.grade = grade;
    }

    
    public String getEmail() {
        return email;
    }

    
    public void setEmail(String email) {
        this.email = email;
    }
    private int regID;
    private String fullName;
    private String year;
    private String facalty;
    private String subject;
    private String code;
    private String assignmentNo;
    private String grade;
    private String email;
    
}
